# --- Listas: Creación y Acceso ---

# Una lista de strings (nombres de frutas)
frutas = ["manzana", "pera", "cereza", True]
print(f"Mi lista de frutas es: {frutas}")

# Para acceder a un elemento, usamos su "índice" (posición).
# ¡Importante! La primera posición en Python siempre es el índice 0.
primera_fruta = frutas[0]
print(f"La primera fruta de la lista (índice 0) es: {primera_fruta}")
#frutas[3] = 2
tercera_fruta = frutas[3]
print(f"La tercera fruta de la lista (índice 3) es: {tercera_fruta}")
