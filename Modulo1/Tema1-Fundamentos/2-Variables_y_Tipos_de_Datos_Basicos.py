# --- Variables y Tipos de Datos Básicos ---

# 1. Entero (int): para números sin decimales.

edad = 30
print("El valor de la variable 'edad' es:", edad)
print("El tipo de dato de 'edad' es:", type(edad))

print("-" * 50) # Un separador para mayor claridad

# 2. Cadena de texto (str): para cualquier texto, se usan comillas.
nombre_completo = "juan lopez"
print("El valor de la variable 'nombre_completo' es:", nombre_completo)
print("El tipo de dato de 'nombre_completo' es:", type(nombre_completo))

print("-" * 20)

# 3. Booleano (bool): solo puede ser True (Verdadero) o False (Falso).
es_mayor_de_edad = False
print("El valor de la variable 'es_mayor_de_edad' es:", es_mayor_de_edad)
print("El tipo de dato de 'es_mayor_de_edad' es:", type(es_mayor_de_edad))
