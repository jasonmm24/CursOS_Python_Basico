# --- 2. El Molde Vacio (La 'class') ---
# OBJETIVO: Entender la sintaxis para crear un "molde".

# 1. 'class' es la palabra clave para definir un nuevo "plano" o "molde".
# 2. 'Coche' es el nombre que le damos a este molde.
#    Por convencion, los nombres de clases siempre empiezan
#    con Mayuscula (se llama 'PascalCase').
# 3. 'pass' es una palabra clave de Python que significa "no hacer nada".
#    La usamos aqui para que la sintaxis de 'class' sea valida,
#    ya que aun no hemos definido sus detalles.

class Coche:
    pass

print("Se ha definido el plano maestro 'Coche'.")
print("No hemos construido nada aun, solo tenemos el diseno.")